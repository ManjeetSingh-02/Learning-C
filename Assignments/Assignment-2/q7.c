#include <stdio.h>

int main() {

    int a , b ;

    printf("Enter 2 Numbers: ") ;
    scanf("%d", &a) ;
    scanf("%d", &b) ;

    printf("a+b Is: %d\n", a+b) ;
    printf("a-b Is: %d\n", a-b) ;
    printf("a*b Is: %d\n", a*b) ;
    printf("a/b Is: %d\n", a/b) ;
    printf("a%%b Is: %d\n", a%b) ;
    
    return 0 ;
}